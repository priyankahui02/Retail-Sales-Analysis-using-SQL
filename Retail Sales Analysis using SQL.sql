-- Rank all products based on total revenue generated using a window function.
SELECT p.product_name,SUM(p.price * o.quantity) AS total_revenue,
RANK() OVER(ORDER BY SUM(p.price * o.quantity) DESC) AS revenue_rank
FROM orders o
JOIN product p
ON o.product_id = p.product_id
GROUP BY p.product_name;

-- Find the second highest revenue generating product.
WITH product_revenue AS (
SELECT p.product_name,SUM(p.price * o.quantity) AS total_revenue
FROM orders AS o
JOIN product AS p
ON o.product_id = p.product_id
GROUP BY p.product_name
)
SELECT product_name, total_revenue
FROM (
SELECT *,
DENSE_RANK() OVER(ORDER BY total_revenue DESC) AS new_rank
FROM product_revenue
) AS ranked_products
WHERE new_rank = 2;

-- Calculate the running total of revenue by order date.
SELECT * FROM customers;
SELECT * FROM orders;
SELECT * FROM product;

SELECT order_date,
SUM(p.price * o.quantity) AS daily_revenue,
SUM(SUM(p.price * o.quantity)) 
OVER (ORDER BY order_date) AS running_revenue
FROM orders AS o
JOIN product AS p
ON o.product_id = p.product_id
GROUP BY order_date
ORDER BY order_date;

-- Find customers whose total spending is above average.
SELECT * FROM customers;
SELECT * FROM orders;
SELECT * FROM product;

WITH customer_total AS
(
SELECT customer_name,SUM(price * quantity) AS total_spending
FROM customers AS c
JOIN orders AS o
ON c.customer_id = o.customer_id
JOIN product AS p
ON o.product_id = p.product_id
GROUP BY customer_name
) 
SELECT customer_name 
FROM customer_total 
WHERE total_spending > (SELECT AVG(total_spending)FROM customer_total)

-- Find the top product in each category using ROW_NUMBER().
SELECT * FROM customers;
SELECT * FROM orders;
SELECT * FROM product;

WITH top_product AS
(
SELECT p.product_name,p.category,p.price,
ROW_NUMBER() OVER(PARTITION BY p.category ORDER BY p.price DESC) AS rank_number
FROM product AS p
)
SELECT product_name, category, price
FROM top_product
WHERE rank_number = 1;