USE sales_analysis_db;


CREATE TABLE customers (
customer_id INT,
customer_name VARCHAR(100),
city VARCHAR(50),
signup_date DATE
);
SELECT * FROM customers;


CREATE TABLE orders (
order_id INT,
customer_id INT,
product_id INT,
quantity INT,
order_date DATE,
payment_method VARCHAR(50)
);
SELECT * FROM orders;

-- Find the total number of orders placed in the orders table.
SELECT COUNT(order_id) AS total_orders
FROM orders;

-- Calculate the total revenue generated from all orders.
SELECT * FROM sales_analysis_db.product;

SELECT SUM(total_revenue) AS company_revenue
FROM 
(
SELECT product_name,SUM(price * quantity) AS total_revenue
FROM Orders AS o
JOIN product AS p
ON o.product_id = p.product_id
GROUP BY product_name
ORDER BY total_revenue DESC
) AS sales;

-- Find the top 5 customers who spent the most money.
SELECT * FROM customers;
SELECT * FROM orders;
SELECT * FROM product;

SELECT customer_name,

CONCAT('₹',FORMAT(SUM(price * quantity),0)) AS total_spent
FROM orders AS o
JOIN customers AS c
ON o.customer_id = c.customer_id
JOIN product AS p
ON o.product_id = p.product_id
GROUP BY customer_name 
ORDER BY SUM(price * quantity) DESC LIMIT 5;

-- Identify the top 5 most sold products by quantity.
SELECT * FROM customers;
SELECT * FROM orders;
SELECT * FROM product;

WITH most_sold_product AS 
(
SELECT product_name,SUM(quantity) AS sold_products
FROM orders AS o
JOIN product AS p
ON o.product_id = p.product_id
GROUP BY product_name
ORDER BY sold_products DESC LIMIT 5
)
SELECT product_name, sold_products
FROM most_sold_product;


-- Calculate the total revenue generated by each product category.
SELECT * FROM customers;
SELECT * FROM orders;
SELECT * FROM product;

WITH product_revenue AS
(
SELECT category,SUM(p.price * o.quantity) AS total_revenue
FROM product AS p
JOIN orders AS o
ON p.product_id = o.product_id
GROUP BY category
ORDER BY total_revenue DESC
)
SELECT category, total_revenue 
FROM product_revenue;

-- Find the monthly sales revenue for the year.
SELECT * FROM customers;
SELECT * FROM orders;
SELECT * FROM product;

SELECT 
DATE_FORMAT(order_date,'%Y-%m') AS month_year,
CONCAT('₹',FORMAT(SUM(price * quantity),0)) AS sales_per_month
FROM orders AS o
JOIN product AS p
ON p.product_id = o.product_id
GROUP BY month_year
ORDER BY sales_per_month DESC;

